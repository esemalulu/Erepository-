    function saveRecord(scriptContext) {
        if (scriptContext.currentRecord.getValue({fieldId: 'custpage_issaving'}))
        {
            alert('You have already submitted this Address. Please wait for it to continue saving.');
            return false;
        }
        var salesOrderRec = record.create({type: record.Type.SALES_ORDER});
        var addressRec = salesOrderRec.getSubrecord({fieldId: 'shippingaddress'});
        var fieldIds = addressRec.getFields();
        for (var i = 0; i < fieldIds.length; i++)
        {
            var field = scriptContext.currentRecord.getField({fieldId: fieldIds[i]});
            if (field != null && field.isMandatory && scriptContext.currentRecord.getValue({fieldId: fieldIds[i]}).length == 0)
            {
                alert('Fields with a red asterix are mandatory. Please make sure to enter data in all mandatory fields.');
                scriptContext.currentRecord.setValue({fieldId: 'custpage_issaving', value: false, ignoreFieldChange: true});
                return false;
            }
        }
        return true;
    }

